    private void carregaTabela(){
        DefaultTableModel modelo = (DefaultTableModel) jTableListarClientes.getModel();
        
        ClienteDAO cdao = new ClienteDAO();
        
        cdao.mostrarClientes().forEach((c) -> {
            modelo.addRow(new Object[]{
                c.getCpf(),
                c.getRg(),
                c.getIdade(),
                c.getNome()
            });
        });

        
    }

